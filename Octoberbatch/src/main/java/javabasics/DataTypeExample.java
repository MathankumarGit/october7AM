package javabasics;

public class DataTypeExample {

    public static void main(String[] args) {
        //datatype variablename;
        //datatype variablename = initialize Value;
       // Whole Number
        byte b = 12;
        short s = 3300;
        int x = 24;
        x = 200;
        long lo = 9087674523l;

        //Decimal Number
        double d = 3.14;
        float fl = 2.6564676f;

        boolean status = true;

        char ch = 'a'; // ASCII Value

        String str = "Learn More";

        System.out.println("Byte Value is " + b);
        System.out.println("Short Value is " + s);

        System.out.println("Int Value is " + x);
        System.out.println("long Value is " + lo);

        System.out.println("float Value is " + fl);
        System.out.println("double Value is " + d);

        System.out.println("Boolean Value is " + status);
        System.out.println("Char Value is " + ch);

        System.out.println("String Value is " + str);

    }
}
